'use strict';
let arr = [1,2,3,4];
let summa = 0;

for (let i = 0; i < 4; i++) {
    if (arr[i]%2 === 0) {
        summa = summa + arr[i];
    }
}

console.log('сумма всех четных элементов в массиве = ', summa);
